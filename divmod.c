{
	return x % y;
}

int32_t mod32(int32_t x, int32_t y)
{
	return x % y;
}


uint8_t udiv8(uint8_t x, uint8_t y)
{
	return x / y;
}

int8_t div8(int8_t x, int8_t y)
{
	return x / y;
}

uint16_t udiv16(uint16_t x, uint16_t y)
{
	return x / y;
}

int16_t div16(int16_t x, int16_t y)
{
	return x / y;
}

uint32_t udiv32(uint32_t x, uint32_t y)
{
	return x / y;
}

int32_t div32(int32_t x, int32_t y)
{
	return x / y;
}
